#!/usr/bin/python
import hmac
import hashlib
import sys

key = '\x73\x7f\xf8\xc2\x1a\xcc\xa1\x57\x64\x95\xe3\xea\xa3\x16\xac\x20\x7c\x25\x72\xb6\xdc\x3a\x97\xd0\x02\x1f\x70\x8e\x40\xf9\xf1\xb0\xe6\x23\xd1\xa0\x0e\xc9\x80\x9b\xad\x0d\x8a\x38\xb5\xe6\x96\xea\x8b\x66\x08\x99\xf9\x16\x1e\x4d\x9b\x91\x04\xfb\xc5\x97\x0e\x57'

with open(sys.argv[1]) as f:
    hsh = hmac.new(key, f.read(), digestmod=hashlib.sha256)
    print(hsh.hexdigest().upper())
